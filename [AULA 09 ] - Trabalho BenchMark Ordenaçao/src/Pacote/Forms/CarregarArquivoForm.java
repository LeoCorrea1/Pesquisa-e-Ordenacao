    private void btnSelecionarArquivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelecionarArquivoActionPerformed
        JFileChooser chooser = new JFileChooser();
    int retorno = chooser.showOpenDialog(this);

    if (retorno == JFileChooser.APPROVE_OPTION) {
        File arquivo = chooser.getSelectedFile();

        numeros = Util.lerArquivo(arquivo.getAbsolutePath());

        if (numeros.length > 0) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Arquivo carregado com sucesso: " + arquivo.getName() +
                "\nQuantidade de números: " + numeros.length);
        } else {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Arquivo carregado, mas sem números válidos.");
        }
    }
    }//GEN-LAST:event_btnSelecionarArquivoActionPerformed

